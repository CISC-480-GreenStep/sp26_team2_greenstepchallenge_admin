-- ============================================================
-- 1. Tables
-- ============================================================

-- Departments / Participant Groups
create table if not exists departments (
  id          bigint generated by default as identity primary key,
  name        text not null,
  description text,
  "createdAt" date not null default current_date
);

-- Users (app-level; mirrors the email of a Supabase auth.users row)
create table if not exists users (
  id           bigint generated by default as identity primary key,
  name         text not null,
  email        text not null unique,
  role         text not null default 'GeneralUser'
                 check (role in ('SuperAdmin', 'Admin', 'GeneralUser')),
  status       text not null default 'Active'
                 check (status in ('Active', 'Deactivated')),
  "groupId"    bigint references departments(id) on delete set null,
  department   text,
  "createdAt"  date not null default current_date,
  "lastActive" date
);

-- Dynamic action categories (Water, Food, Energy, etc.)
create table if not exists categories (
  id           serial primary key,
  name         varchar(255) not null unique,
  description  text,
  "createdAt"  timestamptz default now(),
  "updated_at" timestamptz default now()
);

-- Actions (catalog; not bound to any challenge until linked)
create table if not exists actions (
  id          bigint generated by default as identity primary key,
  name        text not null,
  description text,
  category    text not null,
  points      integer not null default 0
);

-- Challenges
--
-- Color story: `theme` is the legacy single-color field, still read
-- by the dashboard "color dot" widgets. The four bgColor*/txColor*
-- columns power the create/edit form's full palette picker. Both
-- coexist; new rows populate the four, old rows have `theme`.
create table if not exists challenges (
  id                 bigint generated by default as identity primary key,
  name               text not null,
  description        text,
  category           text,
  theme              text default '#4CAF50',
  "bgColorHeader"    varchar(7),
  "txColorHeader"    varchar(7),
  "bgColorBody"      varchar(7),
  "txColorBody"      varchar(7),
  "startDate"        date,
  "endDate"          date,
  "joinBy"           date,
  status             text not null default 'Upcoming'
                       check (status in ('Active', 'Upcoming', 'Completed', 'Archived')),
  "createdBy"        bigint references users(id) on delete set null,
  "participantCount" integer not null default 0,
  "groupId"          bigint references departments(id) on delete set null
);

-- Challenge <-> Actions (many-to-many)
create table if not exists challenge_actions (
  "challengeId" bigint not null references challenges(id) on delete cascade,
  "actionId"    bigint not null references actions(id) on delete cascade,
  primary key ("challengeId", "actionId")
);

-- Challenge <-> Participants (many-to-many)
create table if not exists challenge_participants (
  "challengeId" bigint not null references challenges(id) on delete cascade,
  "userId"      bigint not null references users(id) on delete cascade,
  primary key ("challengeId", "userId")
);

-- Templates (the table the "Templates" UI reads/writes)
create table if not exists templates (
  id                 bigint generated by default as identity primary key,
  name               text not null,
  description        text,
  type               text,
  category           text,
  theme              text default '#4CAF50',
  "createdBy"        bigint references users(id) on delete set null,
  "createdAt"        date not null default current_date,
  "joinBy"           date,
  "participantGroup" bigint references departments(id) on delete set null
);

-- Presets + preset_actions
--
-- Legacy tables from before the templates rename. The "Templates" UI
-- moved to the templates table above; these two haven't been dropped
-- yet because data may still live here. Candidate for cleanup.
create table if not exists presets (
  id          bigint generated by default as identity primary key,
  name        text not null,
  description text,
  category    text,
  theme       text default '#4CAF50',
  status      text default 'Upcoming',
  "createdAt" date not null default current_date
);

create table if not exists preset_actions (
  id          bigint generated by default as identity primary key,
  "presetId"  bigint not null references presets(id) on delete cascade,
  name        text not null,
  description text,
  category    text,
  points      integer not null default 0
);

-- Participation (user action completions)
create table if not exists participation (
  id            bigint generated by default as identity primary key,
  "userId"      bigint not null references users(id) on delete cascade,
  "challengeId" bigint not null references challenges(id) on delete cascade,
  "actionId"    bigint not null references actions(id) on delete cascade,
  "completedAt" date not null,
  notes         text,
  "photoUrl"    text
);

-- Activity Logs (admin audit trail; effectively immutable -- see RLS below)
create table if not exists activity_logs (
  id        bigint generated by default as identity primary key,
  "userId"  bigint references users(id) on delete set null,
  action    text not null,
  timestamp timestamptz not null default now(),
  details   text
);


